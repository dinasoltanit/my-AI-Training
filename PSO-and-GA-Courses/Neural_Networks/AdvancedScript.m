clc;clear;close all
%% Inputs & Targets
%
%   x - input data.
%   y - target data.

x = linspace(0,2*pi,20); %Inputs
y = sin(x); % Targets

% Choose a Training Function
% 'trainlm' is usually fastest.
% 'trainbr' takes longer but may be better for challenging problems.
% 'trainscg' uses less memory. Suitable in low memory situations.
trainFcn = 'trainlm';  % Gradient-Discent backpropagation.

% Create a Fitting Network
hiddenLayerSize = 3;
net = fitnet(hiddenLayerSize,trainFcn);

% Choose Input and Output Pre/Post-Processing Functions
% For a list of all processing functions type: help nnprocess
net.input.processFcns = {'removeconstantrows','mapminmax'}; % Pre-Processing
net.output.processFcns = {'removeconstantrows','mapminmax'}; % Post-Processing

% Setup Division of Data for Training, Validation, Testing
% For a list of all data division functions type: help nndivision
net.divideFcn = 'dividerand';  % Divide data randomly
net.divideMode = 'sample';  % Divide up every sample
net.divideParam.trainRatio = 70/100;
net.divideParam.valRatio = 15/100;
net.divideParam.testRatio = 15/100;

% Choose a Performance Function
% For a list of all performance functions type: help nnperformance
net.performFcn = 'mse';  % Mean Squared Error

% Train the Network
[net,tr] = train(net,x,y);

% Test the Network
y = net(x);
e = gsubtract(y,y);
performance = perform(net,y,y)

% Recalculate Training, Validation and Test Performance
trainTargets = y .* tr.trainMask{1};
valTargets = y .* tr.valMask{1};
testTargets = y .* tr.testMask{1};
trainPerformance = perform(net,trainTargets,y)
valPerformance = perform(net,valTargets,y)
testPerformance = perform(net,testTargets,y)

% View the Network
view(net)

